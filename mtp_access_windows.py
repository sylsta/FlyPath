WPD_OBJECT_SIZE_PID = 11


# ---------------------------------------------------------------------------
# PROPERTYKEY — use the type generated by comtypes from the typelib
# ---------------------------------------------------------------------------
def _find_pk_type(mod):
    for name in ('_tagpropertykey', 'tagPROPERTYKEY', 'PROPERTYKEY', '_PROPERTYKEY'):
        t = getattr(mod, name, None)
        if t is not None:
            return t

    class _PK(ctypes.Structure):
        _fields_ = [("fmtid", GUID), ("pid", c_ulong)]
    return _PK


_PK = _find_pk_type(_wpd)


def _pk(fmtid, pid):
    k = _PK()
    k.fmtid = fmtid
    k.pid = pid
    return k


# ---------------------------------------------------------------------------
# GUID value comparison — ctypes.Structure has no __eq__
# ---------------------------------------------------------------------------
def _guid_eq(a, b):
    try:
        return bytes(a) == bytes(b)
    except Exception:
        return False


# ---------------------------------------------------------------------------
# Vtable helper
# ---------------------------------------------------------------------------
def _vtbl_fn(obj_addr, slot, restype, *argtypes):
    """Return a callable WINFUNCTYPE for vtable slot *slot* of COM object at *obj_addr*."""
    vtbl = ctypes.cast(obj_addr, ctypes.POINTER(ctypes.c_void_p))[0]
    fn = ctypes.cast(ctypes.c_void_p(
               ctypes.cast(vtbl, ctypes.POINTER(ctypes.c_void_p))[slot]),
               ctypes.c_void_p).value
    return ctypes.WINFUNCTYPE(restype, ctypes.c_void_p, *argtypes)(fn)


# ---------------------------------------------------------------------------
# IPortableDeviceManager — GetDevices and GetDeviceFriendlyName via vtable
# (nullable LPWSTR* params that comtypes cannot marshal)
# Vtable after IUnknown (0=QI,1=AddRef,2=Release):
# 3=GetDevices, 4=RefreshDeviceList, 5=GetDeviceFriendlyName, ...
# ---------------------------------------------------------------------------
def _list_device_ids(mgr) -> list:
    addr = ctypes.cast(mgr, ctypes.c_void_p).value
    fn = _vtbl_fn(addr, 3, ctypes.HRESULT, ctypes.c_void_p, ctypes.POINTER(c_ulong))
    count = c_ulong(0)
    fn(addr, None, byref(count))
    if count.value == 0:
        return []
    arr = (c_wchar_p * count.value)()
    fn(addr, ctypes.cast(arr, ctypes.c_void_p), byref(count))
    return [arr[i] for i in range(count.value)]


def _get_friendly_name(mgr, device_id: str) -> str:
    addr = ctypes.cast(mgr, ctypes.c_void_p).value
    fn = _vtbl_fn(addr, 5, ctypes.HRESULT, c_wchar_p, ctypes.c_void_p, ctypes.POINTER(c_ulong))
    length = c_ulong(0)
    fn(addr, device_id, None, byref(length))
    if length.value == 0:
        return device_id
    buf = (ctypes.c_wchar * length.value)()
    fn(addr, device_id, ctypes.cast(buf, ctypes.c_void_p), byref(length))
    return buf.value or device_id


# ---------------------------------------------------------------------------
# IPortableDeviceContent helpers via vtable
# Vtable after IUnknown:
#   3=EnumObjects, 4=Properties, 5=Transfer, 6=CreateObjectWithPropertiesOnly
# ---------------------------------------------------------------------------
def _content_enum_objects(content_addr, parent_id: str):
    """Call EnumObjects and return raw enum pointer address."""
    fn = _vtbl_fn(content_addr, 3, ctypes.HRESULT, c_ulong, c_wchar_p,
                  ctypes.c_void_p, ctypes.POINTER(ctypes.c_void_p))
    out_ptr = ctypes.c_void_p(0)
    hr = fn(content_addr, 0, parent_id, None, byref(out_ptr))
    if hr != 0:
        raise COMError(hr, "EnumObjects failed", ())
    return out_ptr.value


def _content_transfer(content_addr):
    """Call Transfer() and return raw IPortableDeviceResources pointer address."""
    fn = _vtbl_fn(content_addr, 5, ctypes.HRESULT, ctypes.POINTER(ctypes.c_void_p))
    out_ptr = ctypes.c_void_p(0)
    hr = fn(content_addr, byref(out_ptr))
    if hr != 0:
        raise COMError(hr, "Transfer() failed", ())
    return out_ptr.value


# ---------------------------------------------------------------------------
# IEnumPortableDeviceObjectIDs.Next via vtable
# Vtable after IUnknown: 3=Next, 4=Skip, 5=Reset, 6=Clone, 7=Cancel
# ---------------------------------------------------------------------------
def _enum_next(enum_addr, batch=16) -> list:
    """Drain an IEnumPortableDeviceObjectIDs and return all object ID strings."""
    fn = _vtbl_fn(enum_addr, 3, ctypes.HRESULT, c_ulong, ctypes.c_void_p, ctypes.POINTER(c_ulong))
    ids = []
    while True:
        buf = (c_wchar_p * batch)()
        fetched = c_ulong(0)
        hr = fn(enum_addr, batch, ctypes.cast(buf, ctypes.c_void_p), byref(fetched))
        n = fetched.value
        for i in range(n):
            if buf[i]:
                ids.append(buf[i])
        if hr != 0 or n < batch:  # S_FALSE or last batch
            break
    return ids


# ---------------------------------------------------------------------------
# IPortableDeviceResources.GetStream via vtable
# Vtable after IUnknown:
#   3=GetSupportedResources, 4=GetResourceAttributes, 5=GetStream, ...
# Signature: GetStream(LPCWSTR objID, REFPROPERTYKEY key, DWORD mode,
#                      DWORD* optChunk, IStream** ppStream)
# ---------------------------------------------------------------------------
def _resources_get_stream(res_addr, oid: str):
    """Open a read stream for *oid*. Returns (stream_addr, chunk_size)."""
    pk = _pk(_FMTID_RESOURCE, WPD_RESOURCE_DEFAULT_PID)
    optimal_chunk = c_ulong(0)
    stream_ptr = ctypes.c_void_p(0)
    fn = _vtbl_fn(res_addr, 5, ctypes.HRESULT,
                  c_wchar_p,              # pszObjectID
                  ctypes.c_void_p,                 # pKey (REFPROPERTYKEY)
                  c_ulong,                         # dwMode
                  ctypes.POINTER(c_ulong),         # pdwOptimalBufferSize [in,out]
                  ctypes.POINTER(ctypes.c_void_p)  # ppStream [out]
                  )
    hr = fn(res_addr, oid,
            ctypes.cast(ctypes.addressof(pk), ctypes.c_void_p),
            STGM_READ,
            byref(optimal_chunk),
            byref(stream_ptr))
    if hr != 0:
        raise COMError(hr, f"GetStream hr=0x{hr & 0xFFFFFFFF:08X}", ())
    chunk = optimal_chunk.value if optimal_chunk.value > 0 else _CHUNK
    return stream_ptr.value, chunk


# ---------------------------------------------------------------------------
# ISequentialStream.Read via vtable (slot 3 after IUnknown)
# ---------------------------------------------------------------------------
def _com_release(addr):
    """Call IUnknown::Release() (vtable slot 2) on a raw COM pointer address."""
    if addr:
        _vtbl_fn(addr, 2, ctypes.c_ulong)(addr)


def _stream_read_to_file(stream_addr, chunk_size, dst_file: Path):
    """Read stream into dst_file, then Release() the stream."""
    fn = _vtbl_fn(stream_addr, 3, ctypes.HRESULT,
                  ctypes.c_void_p, c_ulong, ctypes.POINTER(c_ulong))
    buf = (ctypes.c_char * chunk_size)()
    dst_file.parent.mkdir(parents=True, exist_ok=True)
    try:
        with open(dst_file, 'wb') as fh:
            while True:
                n_read = c_ulong(0)
                hr = fn(stream_addr,
                        ctypes.cast(buf, ctypes.c_void_p),
                        chunk_size,
                        byref(n_read))
                n = n_read.value
                if n > 0:
                    fh.write(buf[:n])
                if hr != 0 or n == 0:
                    break
    finally:
        _com_release(stream_addr)


# ---------------------------------------------------------------------------
# Write path: GetStream(STGM_WRITE), CreateObjectWithPropertiesAndData,
# ISequentialStream.Write / IStream.SetSize / IStream.Commit via vtable.
# ---------------------------------------------------------------------------
def _resources_get_stream_write(res_addr, oid: str):
    """Open a write stream onto oid's existing default resource. Returns (stream_addr, chunk_size).

    Used to overwrite a file that is already present on the device — this is
    the normal case here, since DJI Fly requires a dummy mission (and its
    .kmz) to already exist before FlyPath can replace its content.
    """
    pk = _pk(_FMTID_RESOURCE, WPD_RESOURCE_DEFAULT_PID)
    optimal_chunk = c_ulong(0)
    stream_ptr = ctypes.c_void_p(0)
    fn = _vtbl_fn(res_addr, 5, ctypes.HRESULT,
                  c_wchar_p, ctypes.c_void_p, c_ulong,
                  ctypes.POINTER(c_ulong), ctypes.POINTER(ctypes.c_void_p))
    hr = fn(res_addr, oid,
            ctypes.cast(ctypes.addressof(pk), ctypes.c_void_p),
            STGM_WRITE,
            byref(optimal_chunk),
            byref(stream_ptr))
    if hr != 0:
        raise COMError(hr, f"GetStream(write) hr=0x{hr & 0xFFFFFFFF:08X}", ())
    chunk = optimal_chunk.value if optimal_chunk.value > 0 else _CHUNK
    return stream_ptr.value, chunk


def _content_create_object_with_data(content_addr, values_addr):
    """Call CreateObjectWithPropertiesAndData. Returns (stream_addr, chunk_size).

    Used only when no object exists yet at the destination path (uncommon
    for this plugin's workflow — see _WPDDevice.upload()).
    """
    fn = _vtbl_fn(content_addr, 7, ctypes.HRESULT,
                  ctypes.c_void_p, ctypes.POINTER(ctypes.c_void_p),
                  ctypes.POINTER(c_ulong), ctypes.c_void_p)
    out_stream = ctypes.c_void_p(0)
    chunk = c_ulong(0)
    hr = fn(content_addr, values_addr, byref(out_stream), byref(chunk), None)
    if hr != 0:
        raise COMError(hr, f"CreateObjectWithPropertiesAndData hr=0x{hr & 0xFFFFFFFF:08X}", ())
    return out_stream.value, (chunk.value or _CHUNK)


def _stream_write_from_file(stream_addr, chunk_size, src_file: Path, total_size: int):
    """Write src_file's bytes into an open write stream, then Commit() and Release().

    Calls SetSize() first so overwriting a file with a *smaller* one doesn't
    leave trailing bytes from the old content after our new data — which
    would corrupt the KMZ's zip central directory for readers that scan from
    the true end of the stream. SetSize is best-effort: some MTP drivers
    don't support it, in which case we log and proceed with Write anyway.
    """
    write_fn = _vtbl_fn(stream_addr, 4, ctypes.HRESULT,
                        ctypes.c_void_p, c_ulong, ctypes.POINTER(c_ulong))
    setsize_fn = _vtbl_fn(stream_addr, 6, ctypes.HRESULT, ctypes.c_uint64)
    commit_fn = _vtbl_fn(stream_addr, 8, ctypes.HRESULT, c_ulong)
    try:
        hr = setsize_fn(stream_addr, ctypes.c_uint64(total_size))
        if hr not in (0, 1):  # not S_OK / S_FALSE — driver refused SetSize
            print(f"[mtp_access_windows] SetSize not supported by this "
                  f"device driver (hr=0x{hr & 0xFFFFFFFF:08X}); proceeding "
                  f"without truncation.")

        with open(src_file, 'rb') as fh:
            while True:
                data = fh.read(chunk_size)
                if not data:
                    break
                buf = ctypes.create_string_buffer(data, len(data))
                n_written = c_ulong(0)
                hr = write_fn(stream_addr, ctypes.cast(buf, ctypes.c_void_p),
                              len(data), byref(n_written))
                if hr != 0:
                    raise COMError(hr, f"Stream Write failed hr=0x{hr & 0xFFFFFFFF:08X}", ())
                if n_written.value != len(data):
                    raise IOError(
                        f"Short write to MTP stream: wrote {n_written.value}/"
                        f"{len(data)} bytes"
                    )

        hr = commit_fn(stream_addr, STGC_DEFAULT)
        if hr != 0:
            raise COMError(hr, f"Stream Commit failed hr=0x{hr & 0xFFFFFFFF:08X}", ())
    finally:
        _com_release(stream_addr)


# ---------------------------------------------------------------------------
# IPortableDeviceProperties helpers
# Comtypes-generated GetValues / GetStringValue / GetGuidValue work correctly
# as long as we pass the right pointer type for PROPERTYKEY.
# ---------------------------------------------------------------------------
def _obj_name(properties, oid: str) -> str:
    try:
        pk = _pk(_FMTID_OBJECT, WPD_OBJECT_NAME_PID)
        return properties.GetValues(oid, None).GetStringValue(ctypes.pointer(pk)) or oid
    except COMError:
        return oid


def _obj_is_folder(properties, oid: str) -> bool:
    try:
        pk = _pk(_FMTID_OBJECT, WPD_OBJECT_CONTENT_TYPE_PID)
        g = properties.GetValues(oid, None).GetGuidValue(ctypes.pointer(pk))
        return _guid_eq(g, WPD_CONTENT_TYPE_FOLDER) or _guid_eq(g, WPD_CONTENT_TYPE_FUNCTIONAL)
    except COMError:
        return False


# ---------------------------------------------------------------------------
# _WPDDevice  — wraps one open device connection
# ---------------------------------------------------------------------------
class _WPDDevice:
    def __init__(self, device_id: str, friendly_name: str):
        self.device_id = device_id
        self.friendly_name = friendly_name
        # Open device
        self._device = comtypes.client.CreateObject(CLSID_PortableDevice,
                                                    interface=IPortableDevice)
        client_info = comtypes.client.CreateObject(CLSID_PortableDeviceValues,
                                                   interface=IPortableDeviceValues)
        self._device.Open(device_id, client_info)
        self._content = self._device.Content()
        # Raw content pointer for vtable calls
        self._content_addr = ctypes.cast(self._content, ctypes.c_void_p).value
        # NOTE: _properties is NOT stored persistently.
        # Keeping IPortableDeviceProperties open while calling GetStream causes
        # ERROR_BUSY (0x800700AA). We acquire it only during enumeration and
        # release it (by letting the local variable go out of scope) before
        # any download.

    def close(self):
        try:
            if self._device:
                self._device.Close()
        except Exception:
            pass
        self._device = self._content = None

    # ------------------------------------------------------------------
    # Object tree
    # ------------------------------------------------------------------

    def _children(self, parent_id: str) -> list:
        """Return list of (oid, name, is_folder) for direct children.

        IPortableDeviceProperties is acquired locally and released
        (goes out of scope) before this method returns, so it does not
        hold the device busy during subsequent GetStream calls.
        """
        enum_addr = _content_enum_objects(self._content_addr, parent_id)
        oids = _enum_next(enum_addr)
        properties = self._content.Properties()  # local — released on return
        result = []
        for oid in oids:
            name = _obj_name(properties, oid)
            is_folder = _obj_is_folder(properties, oid)
            result.append((oid, name, is_folder))
        del properties  # explicitly release before any download
        return result

    def _resolve(self, path: str):
        """Resolve slash-separated path to (oid, is_folder). Returns None if not found."""
        if not path:
            return WPD_DEVICE_OBJECT_ID, True
        parts = [p for p in path.replace('\\', '/').split('/') if p]
        current_id = WPD_DEVICE_OBJECT_ID
        for part in parts:
            found = None
            for oid, name, is_folder in self._children(current_id):
                if name.lower() == part.lower():
                    found = (oid, is_folder)
                    break
            if found is None:
                return None
            current_id, last_is_folder = found
        return current_id, last_is_folder

    def list_folder(self, path: str) -> list:
        res = self._resolve(path)
        if res is None or not res[1]:
            return []
        return [name for _, name, _ in self._children(res[0])]

    # ------------------------------------------------------------------
    # Download
    # ------------------------------------------------------------------

    def _download(self, oid: str, dst: Path):
        """Download one file from the device.

        Each download uses a fresh IPortableDeviceResources (Transfer()) and
        releases both the stream and the resources after use, so the device
        is not held busy for subsequent downloads.
        """
        res_addr = _content_transfer(self._content_addr)
        try:
            stream_addr, chunk = _resources_get_stream(res_addr, oid)
            _stream_read_to_file(stream_addr, chunk, dst)
            # stream is released inside _stream_read_to_file
        finally:
            _com_release(res_addr)

    def _copy_tree(self, oid: str, is_folder: bool, dst: Path):
        """Recursively copy object *oid* to local *dst*."""
        if is_folder:
            dst.mkdir(parents=True, exist_ok=True)
            for child_oid, child_name, child_is_folder in self._children(oid):
                self._copy_tree(child_oid, child_is_folder, dst / child_name)
        else:
            self._download(oid, dst)

    def copy_subtree(self, src_path: str, dst_exact: Path):
        res = self._resolve(src_path)
        if res is None:
            raise FileNotFoundError(f"Not found on {self.friendly_name!r}: {src_path!r}")
        oid, is_folder = res
        if is_folder:
            dst_exact.mkdir(parents=True, exist_ok=True)
            for child_oid, child_name, child_is_folder in self._children(oid):
                self._copy_tree(child_oid, child_is_folder, dst_exact / child_name)
        else:
            self._download(oid, dst_exact)

    # ------------------------------------------------------------------
    # Upload
    # ------------------------------------------------------------------

    def _replace_existing(self, oid: str, src_file: Path):
        """Overwrite an existing object's default resource with src_file's bytes.

        Uses a fresh IPortableDeviceResources per call (Transfer()), released
        afterwards — same pattern as _download(), so the device isn't held
        busy for anything else.
        """
        res_addr = _content_transfer(self._content_addr)
        try:
            size = src_file.stat().st_size
            stream_addr, chunk = _resources_get_stream_write(res_addr, oid)
            _stream_write_from_file(stream_addr, chunk, src_file, size)
            # stream released inside _stream_write_from_file
        finally:
            _com_release(res_addr)

    def _create_and_upload(self, parent_id: str, filename: str, src_file: Path):
        """Create a new object under parent_id and upload src_file's bytes into it."""
        values = comtypes.client.CreateObject(CLSID_PortableDeviceValues,
                                              interface=IPortableDeviceValues)
        pk_parent = _pk(_FMTID_OBJECT, WPD_OBJECT_PARENT_ID_PID)
        pk_name = _pk(_FMTID_OBJECT, WPD_OBJECT_NAME_PID)
        pk_orig = _pk(_FMTID_OBJECT, WPD_OBJECT_ORIGINAL_FILE_NAME_PID)
        pk_type = _pk(_FMTID_OBJECT, WPD_OBJECT_CONTENT_TYPE_PID)
        pk_size = _pk(_FMTID_OBJECT, WPD_OBJECT_SIZE_PID)

        values.SetStringValue(ctypes.pointer(pk_parent), parent_id)
        values.SetStringValue(ctypes.pointer(pk_name), filename)
        values.SetStringValue(ctypes.pointer(pk_orig), filename)
        values.SetGuidValue(ctypes.pointer(pk_type), WPD_CONTENT_TYPE_GENERIC_FILE)
        values.SetUnsignedLargeIntegerValue(ctypes.pointer(pk_size),
                                            src_file.stat().st_size)

        values_addr = ctypes.cast(values, ctypes.c_void_p).value
        stream_addr, chunk = _content_create_object_with_data(
            self._content_addr, values_addr
        )
        _stream_write_from_file(stream_addr, chunk, src_file, src_file.stat().st_size)

    def upload(self, src_local: Path, dst_path: str):
        """Upload a single local file to dst_path (slash-separated) on the device.

        If an object already exists at dst_path, its content is overwritten
        in place (the normal case: DJI Fly requires a dummy mission — and
        its .kmz — to already exist before FlyPath can replace it). If no
        object exists there yet, a new one is created in the parent folder,
        which must already exist.
        """
        if not src_local.is_file():
            raise IsADirectoryError(f"upload() only supports single files, got: {src_local}")

        existing = self._resolve(dst_path)
        if existing is not None and not existing[1]:  # found, and it's a file
            self._replace_existing(existing[0], src_local)
            return

        parts = [p for p in dst_path.replace('\\', '/').split('/') if p]
        if not parts:
            raise ValueError(f"Invalid destination path: {dst_path!r}")
        parent_path = '/'.join(parts[:-1])
        filename = parts[-1]
        parent = self._resolve(parent_path)
        if parent is None or not parent[1]:
            raise FileNotFoundError(
                f"Destination folder not found on {self.friendly_name!r}: "
                f"{parent_path!r}"
            )
        self._create_and_upload(parent[0], filename, src_local)


# ---------------------------------------------------------------------------
# MTPClient — public API
# ---------------------------------------------------------------------------
class MTPClient:
    """Windows WPD MTP client. Same public API as mtp_access_kio_gvfs.MTPClient."""

    def __init__(self):
        self.backend = 'wpd'
        self._registry = {}  # friendly_name -> (device_id, _WPDDevice|None)
        mgr = comtypes.client.CreateObject(CLSID_PortableDeviceManager,
                                           interface=IPortableDeviceManager)
        for dev_id in _list_device_ids(mgr):
            name = _get_friendly_name(mgr, dev_id)
            self._registry[name] = (dev_id, None)
        if not self._registry:
            raise RuntimeError("No WPD/MTP devices found. "
                               "Connect and unlock the device.")

    def _dev(self, name: str) -> _WPDDevice:
        if name not in self._registry:
            raise KeyError(f"Unknown device: {name!r}")
        dev_id, wpd = self._registry[name]
        if wpd is None:
            wpd = _WPDDevice(dev_id, name)
            self._registry[name] = (dev_id, wpd)
        return wpd

    def list_devices(self) -> list:
        return list(self._registry.keys())

    def get_display_name(self, device: str) -> str:
        return device

    def list_root_folders(self) -> dict:
        return {n: self.list_folder(n, '') for n in self._registry}

    def list_folder(self, device: str, path: str) -> list:
        return self._dev(device).list_folder(path)

    def copy_from_device(self, device: str, src_path: str, dst_local: str):
        self.copy_from_device_to_exact(
            device, src_path, str(Path(dst_local) / Path(src_path).name))

    def copy_from_device_to_exact(self, device: str, src_path: str, dst_exact: str):
        self._dev(device).copy_subtree(src_path, Path(dst_exact))

    def copy_to_device(self, src_local: str, device: str, dst_path: str):
        """Upload src_local to dst_path (slash-separated) on device.

        Overwrites the destination's existing content if an object already
        exists there; otherwise creates a new object in the parent folder.
        """
        self._dev(device).upload(Path(src_local), dst_path)


