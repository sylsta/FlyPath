"""
mtp_access_windows.py
=====================
MTP device access on Windows via the native WPD (Windows Portable Devices) COM API.
Uses comtypes.client.GetModule to load interfaces from portabledeviceapi.dll,
with raw vtable calls for methods whose [out] parameters comtypes mishandles.

Public API (same as mtp_access_kio_gvfs.MTPClient):
    client = MTPClient()
    client.list_devices()                           -> ['Pixel 9']
    client.get_display_name('Pixel 9')              -> 'Pixel 9'
    client.list_folder('Pixel 9', '')               -> ['Internal storage']
    client.list_folder('Pixel 9', 'Internal storage/DCIM') -> [...]
    client.copy_from_device_to_exact('Pixel 9', 'Internal storage/...', r'C:\\tmp\\...')
    client.copy_to_device(r'C:\\tmp\\mission.kmz', 'Pixel 9', 'Internal storage/.../mission.kmz')
        -> overwrites in place if the destination already exists (the usual
           case for FlyPath: DJI Fly must already have a dummy mission KMZ
           at that path), otherwise creates a new object in the parent
           folder.
"""

import ctypes
from ctypes import byref, c_ulong, c_wchar_p
from pathlib import Path

import comtypes
import comtypes.client
from comtypes import GUID, COMError

# ---------------------------------------------------------------------------
# Load WPD type library — comtypes generates interface wrappers automatically
# ---------------------------------------------------------------------------
_wpd = comtypes.client.GetModule(r"C:\Windows\System32\portabledeviceapi.dll")
_wpd_types = comtypes.client.GetModule(r"C:\Windows\System32\portabledevicetypes.dll")

IPortableDeviceManager = _wpd.IPortableDeviceManager
IPortableDevice = _wpd.IPortableDevice
IPortableDeviceContent = _wpd.IPortableDeviceContent
IPortableDeviceProperties = _wpd.IPortableDeviceProperties
IPortableDeviceValues = _wpd.IPortableDeviceValues

